package Relaciones;

public class Persona {

    public String nombre;
    public String ci;
    public boolean mayorEdad;
    public Auto auto;

    public Persona(String nombre, String ci, boolean mayorEdad, Auto auto){
        this.nombre = nombre;
        this.ci = ci;
        this.mayorEdad = mayorEdad;
        this.auto = auto;
    }
}


