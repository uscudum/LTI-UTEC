package Herencia;

public class Barco {

    public int latitud;
    public int longitud;
    public double largo;
    public double ancho;

    public Barco(int latitud, int longitud, double largo, double ancho) {
        this.latitud = latitud;
        this.longitud = longitud;
        this.largo = largo;
        this.ancho = ancho;
    }
}

